    public void showData()
    {
        try {
            pst = con.prepareStatement("SELECT * FROM product_review");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            int t;
            
            t = rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)tblProdReview.getModel();
            d.setRowCount(0);
            
            while(rs.next())
            {
                Vector v = new Vector();
                
                for(int i=0; i<=t; i++)
                {
                    v.add(rs.getString("productReviewId"));
                    v.add(rs.getString("customerId"));
                    v.add(rs.getString("productId"));
                    v.add(rs.getString("title"));
                    v.add(rs.getString("review"));
                    v.add(rs.getString("rating"));
                }
                
                d.addRow(v);
            }
            
        } catch (SQLException e) {
            Logger.getLogger(Product_Review.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(this,"Displaying product review data failed.");
        }
    }
    
    public void showProduct()
    {
        try {
            pst = con.prepareStatement("SELECT * FROM product");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            int t;
            
            t = rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)productTbl.getModel();
            d.setRowCount(0);
            
            while(rs.next())
            {
                Vector v = new Vector();
                
                for(int i=0; i<=t; i++)
                {
                    v.add(rs.getString("productId"));
                    v.add(rs.getString("productName"));
                    v.add(rs.getDouble("unitPrice"));
                    v.add(rs.getString("color"));
                    v.add(rs.getString("product_size"));
                }
                
                d.addRow(v);
            }
            
        } catch (SQLException e) {
            Logger.getLogger(Product_Review.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(this,"Displaying product data failed.");
        }
    }

