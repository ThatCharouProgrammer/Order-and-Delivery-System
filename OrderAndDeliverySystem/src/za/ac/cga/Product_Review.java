    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
            
        getData();

        try {
            pst = con.prepareStatement("INSERT INTO product_review (customerId, productId, title, review, rating) VALUES(?,?,?,?,?)");
            pst.setString(1, custId);
            pst.setInt(2, prodId);
            pst.setString(3, title);
            pst.setString(4, review);
            pst.setInt(5, rating);

            int rowsAffected = pst.executeUpdate();
            System.out.println("Number of product review rows inserted: " + rowsAffected);
            
            JOptionPane.showMessageDialog(this,"Record inserted successfully.");
            showData();
            clearFields();
        } catch (SQLException ex) {
            Logger.getLogger(Product_Review.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this,"Product Review insert failed.");
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

